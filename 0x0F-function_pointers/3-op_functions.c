#include "3-calc.h"

/**
 * op_add - sum of two integer number.
 *
 * @a : input number 1.
 * @b : input number 2.
 *
 * Return: sum of two number.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - counting of the subtraction of two number.
 *
 * @a : input number 1.
 * @b : input number 2.
 *
 * Return: the subtraction of two number.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - this function is count multiplication of two number.
 *
 * @a : input number 1.
 * @b : input number 2.
 *
 * Return: multiplication of two number.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - this function is count the devision of two numer.
 *
 * @a : input number 1.
 * @b : input number 2.
 *
 * Return: devision of two numer.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - this function is count the modules number 1 into number 2.
 *
 * @a : input number 1.
 * @b : input number 2.
 *
 * Return: modules number 1 into number 2.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
